'use strict';

/**
 * hookguard — Zero-dependency git hooks manager
 * Library API
 */

const fs = require('fs');
const path = require('path');
const { execSync, spawnSync } = require('child_process');

// All standard git hooks
const VALID_HOOKS = [
  'applypatch-msg',
  'pre-applypatch',
  'post-applypatch',
  'pre-commit',
  'pre-merge-commit',
  'prepare-commit-msg',
  'commit-msg',
  'post-commit',
  'pre-rebase',
  'post-checkout',
  'post-merge',
  'pre-push',
  'pre-receive',
  'update',
  'proc-receive',
  'post-receive',
  'post-update',
  'reference-transaction',
  'push-to-checkout',
  'pre-auto-gc',
  'post-rewrite',
  'sendemail-validate',
  'fsmonitor-watchman',
  'post-index-change'
];

const CONFIG_DIR = '.hookguard';
const HOOKS_SUBDIR = 'hooks';
const HOOKS_DIR = path.join(CONFIG_DIR, HOOKS_SUBDIR);
const CONFIG_FILE = path.join(CONFIG_DIR, 'config.json');

// ─── Internal helpers ────────────────────────────────────────────────────────

function _configPath(cwd) {
  return path.join(cwd, CONFIG_FILE);
}

function _hooksDir(cwd) {
  return path.join(cwd, HOOKS_DIR);
}

function _assertInitialized(cwd) {
  if (!fs.existsSync(_configPath(cwd))) {
    throw new Error(
      'hookguard is not initialized in this directory. Run: hookguard init'
    );
  }
}

function _readConfig(cwd) {
  _assertInitialized(cwd);
  const raw = fs.readFileSync(_configPath(cwd), 'utf8');
  return JSON.parse(raw);
}

function _writeConfig(cwd, config) {
  fs.writeFileSync(_configPath(cwd), JSON.stringify(config, null, 2) + '\n');
}

function _generateHookScript(commands) {
  const lines = [
    '#!/bin/sh',
    '',
    '# Generated by hookguard — do not edit manually',
    '# Manage with: hookguard add / hookguard remove',
    '',
    '# Set SKIP_HOOKS=1 to bypass (e.g. in CI pipelines)',
    'if [ "$SKIP_HOOKS" = "1" ]; then',
    '  exit 0',
    'fi',
    ''
  ];

  for (let i = 0; i < commands.length; i++) {
    const entry = commands[i];
    const label = entry.description || entry.command;
    lines.push(`# [${i + 1}/${commands.length}] ${label}`);
    lines.push(entry.command);
    lines.push('HOOKGUARD_STATUS=$?');
    lines.push('if [ $HOOKGUARD_STATUS -ne 0 ]; then');
    lines.push(`  echo "hookguard: hook failed — ${label}"`);
    lines.push('  exit $HOOKGUARD_STATUS');
    lines.push('fi');
    lines.push('');
  }

  return lines.join('\n');
}

function _writeHookFile(hooksDir, hookName, commands) {
  const hookPath = path.join(hooksDir, hookName);
  const script = _generateHookScript(commands);
  fs.writeFileSync(hookPath, script);
  // Make executable: owner rwx, group rx, others rx
  fs.chmodSync(hookPath, 0o755);
}

function _setGitHooksPath(cwd) {
  try {
    execSync(`git -C "${cwd}" config core.hooksPath "${HOOKS_DIR}"`, {
      stdio: 'pipe'
    });
    return true;
  } catch (_) {
    return false;
  }
}

function _unsetGitHooksPath(cwd) {
  try {
    execSync(`git -C "${cwd}" config --unset core.hooksPath`, {
      stdio: 'pipe'
    });
    return true;
  } catch (_) {
    return false;
  }
}

function _getGitHooksPath(cwd) {
  try {
    return execSync(`git -C "${cwd}" config core.hooksPath`, {
      stdio: 'pipe'
    })
      .toString()
      .trim();
  } catch (_) {
    return null;
  }
}

// ─── Public API ──────────────────────────────────────────────────────────────

/**
 * Initialize hookguard in a git repository.
 * Creates .hookguard/config.json and .hookguard/hooks/, sets core.hooksPath.
 *
 * @param {string} cwd - Working directory (defaults to process.cwd())
 * @returns {{ initialized: boolean, configDir: string, hooksDir: string, gitConfigured: boolean }}
 */
function init(cwd = process.cwd()) {
  const configDir = path.join(cwd, CONFIG_DIR);
  const hooksDir = path.join(cwd, HOOKS_DIR);

  if (fs.existsSync(configDir)) {
    throw new Error(
      'hookguard is already initialized in this directory. ' +
      'Use `hookguard status` to inspect or `hookguard uninstall` to reset.'
    );
  }

  fs.mkdirSync(hooksDir, { recursive: true });

  const config = {
    version: '1.0',
    initializedAt: new Date().toISOString(),
    hooks: {}
  };

  _writeConfig(cwd, config);

  const gitConfigured = _setGitHooksPath(cwd);

  return { initialized: true, configDir, hooksDir, gitConfigured };
}

/**
 * Add a command to a git hook. Creates the hook if it doesn't exist.
 *
 * @param {string} hookName - The git hook name (e.g. 'pre-commit')
 * @param {string} command - The shell command to run (e.g. 'npm test')
 * @param {object} options - { description?: string }
 * @param {string} cwd - Working directory
 * @returns {{ hook: string, command: string, total: number }}
 */
function add(hookName, command, options = {}, cwd = process.cwd()) {
  if (!hookName || typeof hookName !== 'string') {
    throw new Error('Hook name must be a non-empty string');
  }
  if (!VALID_HOOKS.includes(hookName)) {
    throw new Error(
      `"${hookName}" is not a valid git hook. ` +
      `Common hooks: pre-commit, commit-msg, pre-push, prepare-commit-msg`
    );
  }
  if (!command || typeof command !== 'string' || !command.trim()) {
    throw new Error('Command must be a non-empty string');
  }

  const config = _readConfig(cwd);

  if (!config.hooks[hookName]) {
    config.hooks[hookName] = [];
  }

  const entry = {
    command: command.trim(),
    addedAt: new Date().toISOString()
  };

  if (options.description && typeof options.description === 'string') {
    entry.description = options.description.trim();
  }

  config.hooks[hookName].push(entry);
  _writeConfig(cwd, config);
  _writeHookFile(_hooksDir(cwd), hookName, config.hooks[hookName]);

  return {
    hook: hookName,
    command: entry.command,
    total: config.hooks[hookName].length
  };
}

/**
 * Remove all commands for a hook and delete the hook file.
 *
 * @param {string} hookName - The git hook name to remove
 * @param {string} cwd - Working directory
 * @returns {{ removed: string }}
 */
function remove(hookName, cwd = process.cwd()) {
  if (!hookName || typeof hookName !== 'string') {
    throw new Error('Hook name must be a non-empty string');
  }
  if (!VALID_HOOKS.includes(hookName)) {
    throw new Error(`"${hookName}" is not a valid git hook`);
  }

  const config = _readConfig(cwd);

  if (!config.hooks[hookName]) {
    throw new Error(
      `No hook configured for "${hookName}". ` +
      `Run \`hookguard list\` to see what's configured.`
    );
  }

  delete config.hooks[hookName];
  _writeConfig(cwd, config);

  const hookFile = path.join(_hooksDir(cwd), hookName);
  if (fs.existsSync(hookFile)) {
    fs.unlinkSync(hookFile);
  }

  return { removed: hookName };
}

/**
 * List all configured hooks and their commands.
 *
 * @param {string} cwd - Working directory
 * @returns {{ hooks: Array, total: number }}
 */
function list(cwd = process.cwd()) {
  const config = _readConfig(cwd);
  const hooks = [];

  for (const [hookName, commands] of Object.entries(config.hooks)) {
    hooks.push({
      hook: hookName,
      commands: commands.map(c => ({
        command: c.command,
        description: c.description || null,
        addedAt: c.addedAt
      })),
      count: commands.length
    });
  }

  return { hooks, total: hooks.length };
}

/**
 * Manually run a configured hook.
 *
 * @param {string} hookName - The hook to run
 * @param {string} cwd - Working directory
 * @returns {{ hook: string, exitCode: number }}
 */
function run(hookName, cwd = process.cwd()) {
  const config = _readConfig(cwd);

  if (!config.hooks[hookName]) {
    throw new Error(`No hook configured for "${hookName}". Run \`hookguard list\` to see options.`);
  }

  const hookFile = path.join(_hooksDir(cwd), hookName);

  if (!fs.existsSync(hookFile)) {
    throw new Error(
      `Hook file missing: ${hookFile}. ` +
      `Run \`hookguard add ${hookName} "<command>"\` to regenerate it.`
    );
  }

  const result = spawnSync('sh', [hookFile], {
    cwd,
    stdio: 'inherit',
    env: { ...process.env }
  });

  const exitCode = result.status !== null ? result.status : 1;
  return { hook: hookName, exitCode };
}

/**
 * Show the current hookguard status for this directory.
 *
 * @param {string} cwd - Working directory
 * @returns {{ initialized: boolean, hooksCount: number, gitConfigured: boolean, ... }}
 */
function status(cwd = process.cwd()) {
  const initialized = fs.existsSync(_configPath(cwd));

  if (!initialized) {
    return {
      initialized: false,
      hooksCount: 0,
      hooks: [],
      gitConfigured: false,
      gitHooksPath: null
    };
  }

  const config = _readConfig(cwd);
  const hooksCount = Object.keys(config.hooks).length;
  const gitHooksPath = _getGitHooksPath(cwd);
  const gitConfigured = gitHooksPath === HOOKS_DIR;

  return {
    initialized: true,
    hooksCount,
    hooks: Object.keys(config.hooks),
    gitConfigured,
    gitHooksPath,
    version: config.version,
    initializedAt: config.initializedAt
  };
}

/**
 * Remove hookguard from this repository.
 * Deletes .hookguard/ and resets git core.hooksPath.
 *
 * @param {string} cwd - Working directory
 * @returns {{ uninstalled: boolean }}
 */
function uninstall(cwd = process.cwd()) {
  const configDir = path.join(cwd, CONFIG_DIR);

  if (!fs.existsSync(configDir)) {
    throw new Error(
      'hookguard is not initialized in this directory. Nothing to uninstall.'
    );
  }

  _unsetGitHooksPath(cwd);
  fs.rmSync(configDir, { recursive: true, force: true });

  return { uninstalled: true };
}

/**
 * Generate the shell script content for a given list of commands.
 * Exported for testing and inspection.
 */
function generateHookScript(commands) {
  return _generateHookScript(commands);
}

module.exports = {
  init,
  add,
  remove,
  list,
  run,
  status,
  uninstall,
  generateHookScript,
  VALID_HOOKS,
  CONFIG_DIR,
  HOOKS_DIR,
  CONFIG_FILE
};
